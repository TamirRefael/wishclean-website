function getInitialLang() {
  const saved = localStorage.getItem("wishclean_lang");
  if (saved === "he" || saved === "ru") return saved;
  const navLang = (navigator.language || "he").toLowerCase();
  return navLang.startsWith("ru") ? "ru" : "he";
}
